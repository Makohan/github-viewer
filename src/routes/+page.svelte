<h1 class="text-lg font-bold">GitHub Viewer is a simple service for viewing data on GitHub.</h1>

<p class="mt-2">
	This tool is designed for efficiently viewing Issues of a specific GitHub repository.<br />
	By simply providing the information of the repository they wish to investigate as environment variables,
	users can retrieve a list of Issues.<br />(Viewing of Projects is on the to-do list.)
</p>

